const numero1 = 15;
const numero2 = 20;
const numero3 = '25';

// Mayor o igual
console.log(numero1 >= numero2); // false

//Menor o igual
console.log(numero1 <= numero2); // true

//Menor que
console.log(numero1 < numero3); // true 

//Menor que
console.log(numero3 < numero2); // false 

//diferente
console.log(numero3 !== numero1); // true 

//igual
console.log(numero1 === numero2); // false
